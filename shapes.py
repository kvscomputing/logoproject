def house_outline(myTurtle, length, width, x, y):
    myTurtle.setheading(0)
    myTurtle.penup()
    myTurtle.fillcolor("white")
    myTurtle.goto(x, y)
    myTurtle.pendown()
    myTurtle.begin_fill()
    for i in range(2):
        myTurtle.forward(width)
        myTurtle.left(90)
        myTurtle.forward(length)
        myTurtle.left(90)
    myTurtle.penup()
    myTurtle.goto(x, y+length)
    myTurtle.setheading(60)
    myTurtle.pendown()
    myTurtle.forward(width)
    myTurtle.right(120)
    myTurtle.forward(width)
    myTurtle.end_fill()